# 今日完成任务报告的 prompt 模板
# 占位符说明：
# {task_list_json} - 任务清单的 JSON 格式数据
DAILY_REPORT_TODAY_FINISH_PROMPT = """请基于以下今日完成任务清单，按照指定格式输出任务列表。

任务清单（JSON 格式）：
{task_list_json}

输出要求：
1. 仅输出格式化的任务列表，禁止添加任何其他文字、总结、说明或描述
2. 必须严格按照以下格式规则输出：
   - 如果任务有任务族名称（task_group_name）且有子任务（child_task_list），格式为：【任务族名称】任务名称-子任务名称
   - 如果任务有任务族名称（task_group_name）但没有子任务（child_task_list为空），格式为：【任务族名称】任务名称
   - 如果任务没有任务族名称（task_group_name为空或null）但有子任务（child_task_list），格式为：【自添加】任务名称-子任务名称
   - 如果任务没有任务族名称且没有子任务，格式为：【自添加】任务名称
3. 每个任务或子任务占一行，使用换行符分隔
4. 只输出列表，不要添加"今日完成"、"任务列表"等标题或前缀
5. 不要添加总结、评价或其他任何额外内容

示例输出格式：
1.【任务族A】任务1-子任务1
2.【任务族A】任务1-子任务2
3.【任务族B】任务2
4.【自添加】任务3-子任务1
5.【自添加】任务4"""

